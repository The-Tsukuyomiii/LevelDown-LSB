local m = Module:new('Provenance_Mobs')


    m:addOverride('xi.zones.Provenance.Zone.onInitialize',function(zone)
    super(zone)
for _,  entry in pairs(provenanceMobs) do 
        local namez = entry[1]
        local mob  =  zone:insertDynamicEntity({
            objtype  =  xi.objType.MOB,  
            name  =  namez,   
            look  =  entry[4],  
            x  =  entry[5],  
            y  =  entry[6],  
            z  =  entry[7],  
            rotation  =  entry[8],  
            widescan  =  1,  
            groupId  =  entry[2],  
            groupZoneId  =  entry[3],

            onMobSpawn  =  function(mob,   playerArg,   optParams)
                mob:setMobMod(xi.mobMod.ROAM_DISTANCE,   25)
                mob:setMobMod(xi.mobMod.ALWAYS_AGGRO, 1)
                mob:setMobMod(xi.mobMod.CHECK_AS_NM,   1)
                mob:addMod(xi.mod.STR, 100) 
                mob:addMod(xi.mod.VIT, 100) 
                mob:addMod(xi.mod.INT, 100) 
                mob:addMod(xi.mod.MND, 100) 
                mob:addMod(xi.mod.CHR, 100) 
                mob:addMod(xi.mod.AGI, 100) 
                mob:addMod(xi.mod.DEX, 100) 
                mob:addMod(xi.mod.DEF, 100) 
                mob:addMod(xi.mod.RATT, 175) 
                mob:addMod(xi.mod.ACC, 450) 
                mob:addMod(xi.mod.ATT, 550) 
                mob:addMod(xi.mod.MATT, 150)
                mob:addMod(xi.mod.MACC, 450) 
                mob:addMod(xi.mod.MEVA, 250) 
                mob:addMod(xi.mod.MDEF, 150)
                mob:setMod(xi.mod.EVA, 200)
                mob:setMod(xi.mod.DEF, 200)
                if entry[1] == 'Chaos Imp' then
                    mob:setSpellList(522)
                    mob:setMobMod(xi.mobMod.SKILL_LIST, 49999)
                end
            end,  

            onMobFight  =  function(mob, target)
            end,   

            onMobDeath  =  function(mob,   playerArg,   optParams)
                local rand = math.random(1, 2)
                local item = math.random(3498,3499) -- riftross, riftcinder
                    npcUtil.giveItem(playerArg, { { item, rand } })
		            mob:setRespawnTime(180) -- 3 minutes
            end,  
        specialSpawnAnimation  =  false,
        releaseIdOnDisappear = true,            
        })
        mob:setSpawn(entry[5], entry[6], entry[7], entry[8], xi.zone.PROVENANCE)
        mob:setDropID(0) -- loot id
        mob:spawn()
end
    end)

return m
